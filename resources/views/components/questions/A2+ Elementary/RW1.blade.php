            <p>Once there was a man called Jeffrey, who became rich by cheating others. He kept his
                money in bags of gold coins. He had lots of big houses, and always took the gold with
                him when he moved from one house to another.</p>
                <p> One day a young girl named Sarah was walking along a road when she saw a bag. She
                looked inside it and saw that there were lots of gold coins! She took the bag to her father
                Tom, who said, “This must belong to Jeffrey, he’s the only one around here who has bags
                of gold coins! I’ll return them to him tomorrow.” Before he went to bed, he counted the
                coins and saw that there were fifty.</p>
                <p> The next day, he went to see Jeffrey and gave him the bag of coins. Jeffrey counted them
                and saw that there were fifty, but an evil idea came into his mind. “There are only fifty
                coins in this bag, but the bag I lost had one hundred coins! You have stolen fifty coins!
                Give them to me!”</p>
                <p>Tom could not believe what Jeffrey had done and said he didn’t even have one more gold
                coin, let alone fifty. Jeffrey decided to take the matter to the judge.
                “I lost a bag of coins on the road one day”, Jeffrey cried, “and this man’s daughter found
                it and took it to him. Before giving it back to me he stole fifty coins from the bag!”</p>
                <p>The judge knew what kind of man Jeffrey was and thought long and hard. After a few
                minutes he said “Well obviously this bag is not yours, and the bag of one hundred coins
                is still lost. I’ll order the police to look for it. As this bag of fifty coins has not been
                claimed, I order it to be returned to Tom and Sarah.”</p>
