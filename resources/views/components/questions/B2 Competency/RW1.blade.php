        <div class="info mx-auto my-4">
            <p>Non-verbal communication, often referred to as body language, is a means by which we use our
                bodies to communicate. This can be by facial expressions, touch, and body and eye movements.
                When you fold your arms and raise your eyes, for example, you may be expressing disapproval
                without even saying anything. The interesting thing about non-verbal communication is that it’s often
                done with no conscious awareness. We might rub our chin, often when we are interested or engaged,
                without realising that we are doing so. For this reason, non-verbal communication should not be
                confused with sign language, which is commonly used as a means to communicate with people who
                have hearing impairments. Sign language uses specific hand and facial movements to communicate
                specific messages.</p>
            <p>Unfortunately for some of us, not all non-verbal communication is universal, and some movements are
                interpreted differently depending on geographical region. For example, the raising of the index and
                middle fingers, in most cultures, simply means two or twice, but in Great Britain it is an offensive
                gesture. Do you ever place the lower part of your leg over the thigh of your other leg so that the sole of
                your foot can be clearly seen? In most cultures this has little or no meaning, but is offensive in Arabic
                culture, particularly when the sole of the foot is facing the person.
                </p>
            <p>Facial expressions, on the other hand, are usually universal. The most common is when we smile.
                Genuine smiles mean the same to everyone.
                Law enforcement agencies often use non-verbal communication to assess the guilt of a suspect. Even
                though the reliability of lie detection techniques has actually been rejected by many academics, this is
                still common in many countries.</p>
            <p>Gamblers have to be extremely careful that their facial expression, particularly in their eyes, does not
                betray their confidence or lack of. Poker players often try to assess the power of their opponent’s hand
                by scrutinising his or her face. This has led to the expression to have a poker face, meaning to give
                nothing away by facial expression. Unfortunately for some however, not all facial movement can be
                controlled, in particular the nervous tic, where facial muscles move spasmodically.</p>
            <p>The coronavirus pandemic has forced many of us to rethink our use of non-verbal communication.
                The handshake particularly is now unacceptable to many people. Cultures that use kissing the other’s
                cheek once, twice, or even three times have also had to reconsider the appropriacy of such gestures.
                Touching elbow to elbow, or foot to foot, is becoming more commonplace, yet in situations where
                social distancing is required, there can be no touching. The omission of these methods of
                communicating pleasure in meeting or seeing someone could lead to societal problems which cannot
                yet be fully understood. </p>
                
