        <div class="info mx-auto my-4">
            <p>Recent lockdowns imposed due to the threat of coronavirus infections have led to many of us
                spending much more time indoors. Even when lockdowns are relaxed, more of us tend to work or
                study from home, and there are far fewer outdoor events. We can still go outside of course, and many
                of us do, but that pleasant and healthy cycle ride to work, or that live show in the park, or that Sunday
                morning five-a-side football match, has been shelved, for a while, for many of us.</p>
                <p>We can of course exercise and keep fit indoors, and have doors and windows open. We can breathe
                fresh air and enjoy the sunlight streaming through the windows, yet none of this is a real substitute for
                being outside. In 2010, researchers from the University of Essex reported that in studies, just 5
                minutes of outdoor exercise resulted in an improvement in self-esteem.</p>
                <p>There are of course many other reasons for not wishing to be outside than lockdown restrictions.
                Some of us live in densely-populated areas with their accompanying traffic problems. We might have
                to walk for ten minutes or more down noisy and busy streets before we can get to a park or green
                space. If we want to enjoy rivers, lakes and oceans, or hills and mountains, we usually have to get
                there by car or public transport, and either of these can act as a deterrent, particularly if the journey is
                very long. In summer, flies, wasps and mosquitos also put you off. How many times has that eagerly
                anticipated outdoor picnic been spoiled by wasps? Easier just to stay indoors!</p>
                <p>Despite these obstacles however, we really do need to get outside more. As I mentioned before, it’s
                not all about exercise. You can do that at a gym or at home. There are thousands of videos on
                YouTube which feature dance classes, aerobic routines and even yoga. Exercising at home has
                additional benefits such as not having to worry about what you’re wearing (jumping jacks in your
                underwear!) and being able to hop in the shower as soon as you’re done.</p>
                <p>So next time you go outside for pleasure, how about just reading a book under a tree, or just lying in
                the sun?</p>
                
